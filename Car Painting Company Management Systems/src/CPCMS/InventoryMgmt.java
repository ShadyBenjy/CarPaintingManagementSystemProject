    public void refresh_JTable() {
        
           List payments = CarPaintingCompanyManagementSystems.payment;
           DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
           Object rowData[] = new Object[7]; 
           Iterator itr = payments.iterator();
           payment pay;          
           model.setNumRows(0); //Remove all existing rows of JTable
           //Add every element of the List Array as a new row into JTable
           while (itr.hasNext()) {
              pay = (payment) itr.next();
              rowData[0] = pay.getPay_id();
              rowData[1] = pay.getCli_id();
              rowData[2] = pay.getPay_amount();
              rowData[3] = pay.getPay_date();
               
              model.addRow(rowData);
           }   
          }

