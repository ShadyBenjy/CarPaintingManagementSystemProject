    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(
           !jTextField1.getText().trim().isEmpty()&&
           !jTextField2.getText().trim().isEmpty()&&
           !jTextField3.getText().trim().isEmpty()&&
           !jTextField4.getText().trim().isEmpty()) {
            int prod_id         = Integer.parseInt(jTextField1.getText().trim());
            int dept_id      = Integer.parseInt(jTextField2.getText().trim());
            String prod_name    = jTextField3.getText().trim();
            String prod_code = jTextField4.getText().trim();
       
            
            
           List inventorys = CarPaintingCompanyManagementSystems.inventory; 
           inventory st;
           Boolean found=false;
           Iterator <inventory> itr = inventorys.iterator();
           while (itr.hasNext()) {
              st = itr.next(); 
              if(prod_id==st.getProd_id()) {
                found=true;
                break;
              }
            }
           if (!found) {  
             // New Student Record  
            CarPaintingCompanyManagementSystems.add_inventory(prod_id, dept_id, prod_name, prod_code);
             JOptionPane.showMessageDialog(null, "NEW Products Record Successfully ADDED!");
           } else {
             // found is true! So existing student recort will be updated!!!  
             CarPaintingCompanyManagementSystems.edit_inventory(prod_id, dept_id, prod_name, prod_code);
             JOptionPane.showMessageDialog(null, "Product Record Successfully EDITED!");

           }
    }                                        
        else {
            JOptionPane.showMessageDialog(null, "You have to fill all fields before saving!");
        }
        refresh_JTable();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
         if(
           !jTextField1.getText().trim().isEmpty()) {
            int prod_id         = Integer.parseInt(jTextField1.getText().trim());
            
           List inventorys = CarPaintingCompanyManagementSystems.inventory; 
           inventory st;
           Boolean found=false;
           Iterator <inventory> itr = inventorys.iterator();
           while (itr.hasNext()) {
              st = itr.next(); 
              if(prod_id==st.getProd_id()) {
                found=true;
                break;
              }
            }
           if (found) {  
             CarPaintingCompanyManagementSystems.delete_inventory(prod_id);
             JOptionPane.showMessageDialog(null, "Selected Product Record Successfully DELETED!");
           } 
    } else                                      
       {
            JOptionPane.showMessageDialog(null, "You have to fill Student id field before deleting the record!");
        }
        refresh_JTable();
    }//GEN-LAST:event_jButton3ActionPerformed

