    public void refresh_JTable() {
        
           List clients = CarPaintingCompanyManagementSystems.client;
           DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
           Object rowData[] = new Object[7]; 
           Iterator itr = clients.iterator();
           client cli;          
           model.setNumRows(0); //Remove all existing rows of JTable
           //Add every element of the List Array as a new row into JTable
           while (itr.hasNext()) {
              cli = (client) itr.next();
              rowData[0] = cli.getCli_id();
              rowData[1] = cli.getCli_name();
              rowData[2] = cli.getCli_city();
              rowData[3] = cli.getCli_contact();
               
              model.addRow(rowData);
           }   
          }
