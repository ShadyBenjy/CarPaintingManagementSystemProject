    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
     if(
           !jTextField1.getText().trim().isEmpty()&&
           !jTextField2.getText().trim().isEmpty()&&
           !jTextField3.getText().trim().isEmpty()&&
           !jTextField4.getText().trim().isEmpty()) {
            int cli_id         = Integer.parseInt(jTextField1.getText().trim());
            String cli_name      = jTextField2.getText().trim();
            String cli_city    = jTextField3.getText().trim();
            String cli_contact = jTextField4.getText().trim();
            
            
           List clients = CarPaintingCompanyManagementSystems.client; 
           client st;
           Boolean found=false;
           Iterator <client> itr = clients.iterator();
           while (itr.hasNext()) {
              st = itr.next(); 
              if(cli_id==st.getCli_id()) {
                found=true;
                break;
              }
            }
           if (!found) {  
             // New Student Record  
             CarPaintingCompanyManagementSystems.add_client(cli_id, cli_name, cli_city, cli_contact);
             JOptionPane.showMessageDialog(null, "NEW Client Record Successfully ADDED!");
           } else {
             // found is true! So existing student recort will be updated!!!  
             CarPaintingCompanyManagementSystems.edit_client(cli_id, cli_name, cli_city, cli_contact);
             JOptionPane.showMessageDialog(null, "Client Record Successfully EDITED!");

           }
    }                                        
        else {
            JOptionPane.showMessageDialog(null, "You have to fill all fields before saving!");
        }
        refresh_JTable();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(
           !jTextField1.getText().trim().isEmpty()) {
            int cli_id         = Integer.parseInt(jTextField1.getText().trim());
            
           List students = CarPaintingCompanyManagementSystems.client; 
           client st;
           Boolean found=false;
           Iterator <client> itr = students.iterator();
           while (itr.hasNext()) {
              st = itr.next(); 
              if(cli_id==st.getCli_id()) {
                found=true;
                break;
              }
            }
           if (found) {  
             CarPaintingCompanyManagementSystems.delete_client(cli_id);
             JOptionPane.showMessageDialog(null, "Selected Student Record Successfully DELETED!");
           } 
    } else                                      
       {
            JOptionPane.showMessageDialog(null, "You have to fill Student id field before deleting the record!");
        }
        refresh_JTable();
    }//GEN-LAST:event_jButton3ActionPerformed

