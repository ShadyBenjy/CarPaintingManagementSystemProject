    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(
           !jTextField1.getText().trim().isEmpty()&&
           !jTextField2.getText().trim().isEmpty()&&
           !jTextField3.getText().trim().isEmpty()&&
           !jTextField4.getText().trim().isEmpty()&&
           !jTextField5.getText().trim().isEmpty()) {
            int emp_id         = Integer.parseInt(jTextField1.getText().trim());
            String emp_name      = jTextField2.getText().trim();
            String emp_surname    = jTextField3.getText().trim();
            String gender = jTextField4.getText().trim();
            String birthday = jTextField5.getText().trim();
            
            
           List employees =CarPaintingCompanyManagementSystems.employee; 
           employee st;
           Boolean found=false;
           Iterator <employee> itr = employees.iterator();
           while (itr.hasNext()) {
              st = itr.next(); 
              if(emp_id==st.getEmp_id()) {
                found=true;
                break;
              }
            }
           if (!found) {  
             // New Student Record  
             CarPaintingCompanyManagementSystems.add_employee(emp_id, emp_name, emp_surname, gender, birthday);
             JOptionPane.showMessageDialog(null, "NEW Employee Record Successfully ADDED!");
           } else {
             // found is true! So existing student recort will be updated!!!  
             CarPaintingCompanyManagementSystems.edit_employee(emp_id, emp_name, emp_surname, gender, birthday);
             JOptionPane.showMessageDialog(null, "Employee Record Successfully EDITED!");

           }
    }                                        
        else {
            JOptionPane.showMessageDialog(null, "You have to fill all fields before saving!");
        }
        refresh_JTable();
    }//GEN-LAST:event_jButton2ActionPerformed

