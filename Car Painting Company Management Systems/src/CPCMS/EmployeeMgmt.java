    public void refresh_JTable() {
        
           List employees = CarPaintingCompanyManagementSystems.employee;
           DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
           Object rowData[] = new Object[7]; 
           Iterator itr = employees.iterator();
           employee emp;          
           model.setNumRows(0); //Remove all existing rows of JTable
           //Add every element of the List Array as a new row into JTable
           while (itr.hasNext()) {
              emp = (employee) itr.next();
              rowData[0] = emp.getEmp_id();
              rowData[1] = emp.getEmp_name();
              rowData[2] = emp.getEmp_surname();
              rowData[3] = emp.getGender();
              rowData[5] = emp.getBirthday();    
              model.addRow(rowData);
           }   
          }

